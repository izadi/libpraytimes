package org.praytimes.www;

public class Date {
	private final int year; // full
	private final int month; // starting from 1
	private final int day; // starting from 1

	public Date(int year, int month, int day) {
		this.year = year;
		this.month = month;
		this.day = day;
	}

	public int getYear() {
		return year;
	}

	public int getMonth() {
		return month;
	}

	public int getDay() {
		return day;
	}
}
